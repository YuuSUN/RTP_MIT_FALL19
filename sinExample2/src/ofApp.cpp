//--------------------------------------------------------------
void ofApp::draw(){
    
    ofBackground(0);
    
    float time = ofGetElapsedTimef();
    float y = ofMap(sin(time*2), -1, 1, 200,600);
    ofDrawCircle(400, y, 50);
    
    y = ofMap(sin(time*2+1), -1, 1, 200,600);
    ofDrawCircle(200, y, 50);
    y = ofMap(sin(time*2+2), -1, 1, 200,600);
    ofDrawCircle(600, y, 50);
    
}

